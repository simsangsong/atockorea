#!/usr/bin/env node
// Build-time generator: scan all per-locale tour-product JSON files, extract
// ONLY the catalog_card field (+ price.amountLabel needed for fallback price
// parsing), emit a thin TS module under
// components/product-tour-static/catalog/catalogCards.generated.ts.
//
// Why: the full registry imports ~25MB of JSON across 204 files for the home
// page's catalog cards, which only need ~40KB of catalog_card data. The
// generated module unblocks home callers from pulling the entire detail
// payload into the initial bundle.
//
// Run: node scripts/build-catalog-cards.mjs
//      (also runs automatically via `npm run prebuild` — see package.json)
//
// Output file is committed so CI doesn't need to regenerate.

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.resolve(__dirname, "..");
const SRC_DIR = path.join(REPO, "components", "product-tour-static");
const OUT = path.join(SRC_DIR, "catalog", "catalogCards.generated.ts");

const LOCALES = ["en", "ko", "zh", "zh-TW", "es", "ja"];

/** Pull catalog_card + price.amountLabel from one JSON file. */
async function readSlimPage(jsonPath) {
  const raw = await fs.readFile(jsonPath, "utf8");
  const data = JSON.parse(raw);
  const cc = data.catalog_card;
  if (!cc || typeof cc !== "object") return null;
  return {
    catalog_card: {
      slug: cc.slug,
      title: cc.title,
      subtitle: cc.subtitle,
      region: cc.region,
      duration: cc.duration,
      stopsCount: cc.stopsCount,
      rating: cc.rating,
      reviewCount: cc.reviewCount,
      badges: cc.badges,
      heroImage: cc.heroImage,
      thumbnail: cc.thumbnail,
      priceLabel: cc.priceLabel,
      shortCardDescription: cc.shortCardDescription,
    },
    price:
      data.price && typeof data.price.amountLabel === "string"
        ? { amountLabel: data.price.amountLabel }
        : undefined,
  };
}

/** Discover (slug, locale) → file mapping by scanning per-slug folders. */
async function discover() {
  const entries = await fs.readdir(SRC_DIR, { withFileTypes: true });
  // map: locale -> { slug: SlimPage }
  const result = Object.fromEntries(LOCALES.map((l) => [l, {}]));
  const slugSet = new Set();
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const slugDir = path.join(SRC_DIR, entry.name);
    let files;
    try {
      files = await fs.readdir(slugDir);
    } catch {
      continue;
    }
    for (const file of files) {
      const m = file.match(/^(.+?)\.([a-z]{2}(?:-[A-Z]{2})?)\.json$/);
      if (!m) continue;
      const [, slug, locale] = m;
      if (!LOCALES.includes(locale)) continue;
      const filePath = path.join(slugDir, file);
      const slim = await readSlimPage(filePath);
      if (!slim) continue;
      // slug from filename should match catalog_card.slug; trust the JSON.
      const finalSlug = slim.catalog_card.slug || slug;
      result[locale][finalSlug] = slim;
      slugSet.add(finalSlug);
    }
  }
  return { byLocale: result, slugs: [...slugSet].sort() };
}

/**
 * D1 (2026-07-05): besides the combined module, emit
 *   - catalogCards.slugs.generated.ts  (slug order only, ~1KB)
 *   - catalogCards.<locale>.generated.ts  (one locale's pages each)
 * so client surfaces can ship EN only and lazy-import the active locale
 * (the combined 6-locale module is ~275KB / ~60KB gz — it stays for server
 * callers and not-yet-migrated consumers).
 */
function emitSlugsTs(slugs) {
  return `// AUTO-GENERATED by scripts/build-catalog-cards.mjs — DO NOT EDIT.
// Slug order only — safe to import from client bundles without dragging
// the catalog card data along.

export const SLIM_CATALOG_SLUG_ORDER: readonly string[] = ${JSON.stringify(slugs, null, 2)} as const;
`;
}

function emitLocaleTs(locale, pages) {
  return `// AUTO-GENERATED by scripts/build-catalog-cards.mjs — DO NOT EDIT.
// catalog_card pages for the "${locale}" locale only. Loaded lazily by
// staticTourCatalogCards.lazy.ts so client bundles ship one locale, not six.

import type { SlimCatalogPage } from "./catalogCards.generated";

export const PAGES: Record<string, SlimCatalogPage> = ${JSON.stringify(pages, null, 2)};
`;
}

function emitTs({ byLocale, slugs }) {
  const header = `// AUTO-GENERATED by scripts/build-catalog-cards.mjs — DO NOT EDIT.
// Slim subset (catalog_card + price.amountLabel only) of the per-slug
// tour-product JSON files. Total size is ~40KB — safe for home initial
// JS bundle. The full detail JSON stays in staticTourProductRegistry.ts
// and is loaded only by /tour-product/[slug].

export type SlimCatalogCard = {
  slug: string;
  title: string;
  subtitle: string;
  region: string;
  duration: string;
  stopsCount: number;
  rating: number;
  reviewCount: number;
  badges: readonly string[];
  heroImage: string;
  thumbnail: string;
  priceLabel: string;
  shortCardDescription: string;
};

export type SlimCatalogPage = {
  catalog_card: SlimCatalogCard;
  price?: { amountLabel?: string };
};

export const SLIM_CATALOG_SLUG_ORDER: readonly string[] = ${JSON.stringify(
    slugs,
    null,
    2,
  )} as const;

export const SLIM_CATALOG_PAGES_BY_LOCALE: Record<string, Record<string, SlimCatalogPage>> = ${JSON.stringify(
    byLocale,
    null,
    2,
  )};
`;
  return header;
}

async function main() {
  console.log(`[catalog-cards] scanning ${SRC_DIR} …`);
  const data = await discover();
  console.log(
    `[catalog-cards] found ${data.slugs.length} slugs across ${LOCALES.length} locales`,
  );
  const ts = emitTs(data);
  await fs.writeFile(OUT, ts, "utf8");
  const stat = await fs.stat(OUT);
  console.log(
    `[catalog-cards] wrote ${OUT} (${(stat.size / 1024).toFixed(1)} KB)`,
  );

  const slugsOut = path.join(SRC_DIR, "catalog", "catalogCards.slugs.generated.ts");
  await fs.writeFile(slugsOut, emitSlugsTs(data.slugs), "utf8");
  console.log(`[catalog-cards] wrote ${slugsOut}`);

  for (const locale of LOCALES) {
    const localeOut = path.join(
      SRC_DIR,
      "catalog",
      `catalogCards.${locale}.generated.ts`,
    );
    await fs.writeFile(localeOut, emitLocaleTs(locale, data.byLocale[locale]), "utf8");
    const localeStat = await fs.stat(localeOut);
    console.log(
      `[catalog-cards] wrote ${localeOut} (${(localeStat.size / 1024).toFixed(1)} KB)`,
    );
  }
}

main().catch((err) => {
  console.error("[catalog-cards] failed:", err);
  process.exit(1);
});
