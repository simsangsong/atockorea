/**
 * 日本語 tour_product_pages upsert SQL 生成
 * - ソース: components/.../southwest-tour-product-full-page.ja.json（page_sections 除去）
 * - sectionUi: east-signature ja full-page + 西南ルート用 fit 文言オーバーライド
 * - price_per: copywriter が "person" の場合は `人` に統一
 *
 * Run: node scripts/gen-southwest-tour-product-ja-sql.mjs
 */

import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const jaPath = join(
  root,
  "components/product-tour-static/southwest-hallasan-osulloc-aewol/southwest-tour-product-full-page.ja.json",
);
const eastJaPath = join(root, "supabase/manual/generated/east-signature-nature-core-full-page.ja.json");
const outPath = join(root, "supabase/manual/upsert-southwest-hallasan-osulloc-aewol-ja.sql");

const SLUG = "southwest-hallasan-osulloc-aewol";
const LOCALE = "ja";

function sqlEscapeLiteral(s) {
  return String(s).replace(/'/g, "''");
}

const raw = JSON.parse(readFileSync(jaPath, "utf8"));
if (raw.page_sections) delete raw.page_sections;
raw.locale = "ja";

const eastJa = JSON.parse(readFileSync(eastJaPath, "utf8"));
const baseSectionUi = eastJa.sectionUi && typeof eastJa.sectionUi === "object" ? { ...eastJa.sectionUi } : {};

raw.sectionUi = {
  ...baseSectionUi,
  fitFamiliesText:
    "8歳以上向け。短い登山コースと階段区間に対応できる方におすすめです。",
  fitSeniorsText:
    "オスンセンアクと天帝淵の滝はペースを落とせば多くの方に参加しやすいです。",
};

function normalizePricePer(obj) {
  if (!obj || typeof obj !== "object") return;
  const p = obj.per;
  if (p === "person" || p === "Person") obj.per = "人";
}

normalizePricePer(raw.price);
normalizePricePer(raw.sticky_booking_bar?.price);

const cc = raw.catalog_card;
const seo = raw.seo;
const price = raw.price;

const detailPayloadJson = JSON.stringify(raw);
const detailPayloadSql = sqlEscapeLiteral(detailPayloadJson);

const badgesArray = (cc.badges || []).map(sqlEscapeLiteral).join("', '");

const pricePerSql = sqlEscapeLiteral(String(price?.per ?? "人"));

const sql = `-- =============================================================================
-- Southwest Hallasan / Osulloc / Aewol — tour_product_pages (日本語, locale=ja)
-- =============================================================================
-- Slug: ${SLUG} | locale: ${LOCALE}
-- Idempotent: ON CONFLICT (slug, locale) DO UPDATE
-- Prerequisites: en 行 + tours 行が既にあること（tour_id 連携）
--
-- Generated by: node scripts/gen-southwest-tour-product-ja-sql.mjs
-- =============================================================================

BEGIN;

INSERT INTO public.tour_product_pages (
  slug,
  locale,
  product_id,
  is_published,
  sort_order,
  tour_id,
  title,
  subtitle,
  region_label,
  duration_label,
  stops_count,
  rating_avg,
  review_count,
  badges,
  hero_image_url,
  thumbnail_url,
  card_short_description,
  seo_title,
  meta_description,
  headline_line_1,
  headline_line_2,
  price_amount_label,
  price_currency,
  price_per,
  detail_payload
)
SELECT
  '${SLUG}',
  '${LOCALE}',
  '${SLUG}',
  TRUE,
  COALESCE((SELECT sort_order FROM public.tour_product_pages WHERE slug = '${SLUG}' AND locale = 'en' LIMIT 1), 1),
  (SELECT id FROM public.tours WHERE slug = '${SLUG}' LIMIT 1),
  '${sqlEscapeLiteral(cc.title)}',
  '${sqlEscapeLiteral(cc.subtitle)}',
  '${sqlEscapeLiteral(cc.region)}',
  '${sqlEscapeLiteral(cc.duration)}',
  ${Number(cc.stopsCount) || 6},
  ${Number(cc.rating) || 4.8},
  ${Number(cc.reviewCount) || 127},
  ARRAY['${badgesArray}']::text[],
  '${sqlEscapeLiteral(cc.heroImage)}',
  '${sqlEscapeLiteral(cc.thumbnail)}',
  '${sqlEscapeLiteral(cc.shortCardDescription)}',
  '${sqlEscapeLiteral(seo.pageTitle)}',
  '${sqlEscapeLiteral(seo.metaDescription)}',
  '${sqlEscapeLiteral(raw.headlineLine1)}',
  '${sqlEscapeLiteral(raw.headlineLine2)}',
  '${sqlEscapeLiteral(String(price.amountLabel))}',
  '${sqlEscapeLiteral(price.currency)}',
  '${pricePerSql}',
  '${detailPayloadSql}'::jsonb
ON CONFLICT (slug, locale) DO UPDATE SET
  product_id = EXCLUDED.product_id,
  is_published = EXCLUDED.is_published,
  sort_order = EXCLUDED.sort_order,
  tour_id = EXCLUDED.tour_id,
  title = EXCLUDED.title,
  subtitle = EXCLUDED.subtitle,
  region_label = EXCLUDED.region_label,
  duration_label = EXCLUDED.duration_label,
  stops_count = EXCLUDED.stops_count,
  rating_avg = EXCLUDED.rating_avg,
  review_count = EXCLUDED.review_count,
  badges = EXCLUDED.badges,
  hero_image_url = EXCLUDED.hero_image_url,
  thumbnail_url = EXCLUDED.thumbnail_url,
  card_short_description = EXCLUDED.card_short_description,
  seo_title = EXCLUDED.seo_title,
  meta_description = EXCLUDED.meta_description,
  headline_line_1 = EXCLUDED.headline_line_1,
  headline_line_2 = EXCLUDED.headline_line_2,
  price_amount_label = EXCLUDED.price_amount_label,
  price_currency = EXCLUDED.price_currency,
  price_per = EXCLUDED.price_per,
  detail_payload = EXCLUDED.detail_payload,
  updated_at = NOW();

COMMIT;

SELECT slug, locale, is_published, product_id, left(meta_description, 60) AS meta_preview
FROM public.tour_product_pages
WHERE slug = '${SLUG}' AND locale = '${LOCALE}';
`;

writeFileSync(outPath, sql, "utf8");
console.log("Wrote", outPath);
